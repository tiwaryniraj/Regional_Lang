#include <stdio.h>

int main()
{
    int num1 = 12;
    int num2 = 7;

    int sum = num1 + num2;

    int diff = num1 - num2;
    int product = num1 * num2;
    int quotient = num1 / num2;
    int remainder = 12 % 7;

    printf("Sum : %d\n", sum);
    printf("Difference : %d\n", diff);
    printf("Product : %d\n", product);
    printf("Quotient : %d\n", quotient);
    printf("Remainder : %d\n", remainder);
}
