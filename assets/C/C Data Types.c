#include <stdio.h>

int main()
{
    int num = 12;
    printf("%d", num);
    printf("\n");

    float fl = 1.25;
    printf("%f", fl);
    printf("\n");

    char ch = 'a';
    printf("%c", ch);
    printf("\n");

    char name[] = "Abc";
    printf("%s", name);
    printf("\n");
}
