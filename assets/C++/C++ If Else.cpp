#include <bits/stdc++.h>
using namespace std;

int main()
{
    int num = 12;

    if (num == 12) {
        cout << "num is equal to 12";
    }
    else if (num == 13) {
        cout << "num is equal to 13";
    }
    else if (num == 14) {
        cout << "num is equal to 14";
    }
    else {
        cout << "num : " << num;
    }
}
